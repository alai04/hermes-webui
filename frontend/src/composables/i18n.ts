type LocaleValue = typeof LOCALES['en'][string]
type LocaleFn = (...args: any[]) => string

let currentLocale = 'en'

export function setLocale(locale: string) {
  if (LOCALES[locale as keyof typeof LOCALES]) {
    currentLocale = locale
  }
}

export function getLocale() {
  return currentLocale
}

export function t(key: LocaleKey, ...args: any[]): string {
  const locales = LOCALES[currentLocale as keyof typeof LOCALES] || LOCALES.en
  const val = (locales as any)[key] ?? (LOCALES.en as any)[key]
  if (val === undefined) return key
  if (typeof val === 'function') return (val as LocaleFn)(...args)
  return String(val)
}

export function getAllLocales() {
  return Object.entries(LOCALES).map(([code, data]) => ({
    code,
    label: (data as any)._label,
  }))
}
